export function lookupZip3(zip: string): { city: string; state: string } | null {
  if (!zip || zip.length < 3) return null;
  const prefix = zip.substring(0, 3);
  const info = zip3Mapping[prefix];
  
  if (!info) return null;
  
  const [city, state] = info.split(", ");
  return { city, state: state || "" };
}
